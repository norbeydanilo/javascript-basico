// SUMA
let a = 3;
let b = 2;
let z = a + b;

console.log("Resultado de la suma: " + z);

// RESTA
z = a - b;
console.log("Resultado de la resta: " + z);

// MULTIPLICACIÓN
z = a * b;
console.log("Resultado de la multiplicación: " + z);

// DIVISIÓN
z = a / b;
console.log("Resultado de la división: " + z);

// MÓDULO - Residuo o resto
z = a % b;
console.log("Resultado del módulo o residuo: " + z);

// EXPONENTE
z = a ** b;
console.log("Resultado del exponente: " + z);

// INCREMENTO
z = a++;
console.log("Resultado del incremento: " + a);

// DECREMENTO
z = a--;
console.log("Resultado del decremento: " + a);

/*Incremento y decremento*/

// Preincremento -- aumenta antes de cualquier operación
let i = 0;
console.log(++i);

// Posincremento -- aumenta después de haber utilizado el valor actual
let j = 0;
console.log(j++);
console.log(j);

// Predecremento -- disminuye antes de cualquier operación
let k = 3;
console.log(--k);

// Posdecremento -- disminuye después de haber utilizado el valor actual
let m = 3;
console.log(m--);
console.log(m); 